ZZRGUTEX ;RGI/CBR - Unit Tests - RPC Broker ;02/07/2013
 ;;1.0;UNIT TEST;;Apr 25, 2012;Build 1
 TSTART
 I $T(EN^XTMUNIT)'="" D EN^XTMUNIT("ZZRGUTEX")
 TROLLBACK
 Q
 ;
STARTUP ;
 N GMPIFN1,GMPIFN2
 S DTIME=500 ;D INIT^GMPLMGR
 K XUSER,XOPT
 S DUZ=$$CHECKAV^XUSRB("fakedoc1;1Doc!@#$")
 S GMPLUSER=1
 D DUZ^XUS1A
 S GMPVAMC=DUZ(2)
 S GMPROV=DUZ
 S DT=$P($$HTFM^XLFDT($H),".")
 S U="^"
 S GMPCLIN=$O(^SC("B","VISTA HEALTH CARE",""))_"^VISTA HEALTH CARE"
 D NEWPAT
 Q
 ;
SHUTDOWN ;
 Q
 ;
NEWPAT ;
 N PAT,PATU,PATDFN,ERR
 S PAT(.01)="GMPLTEST, PATIENT"
 S PAT(.02)="M"
 S PAT(.03)=2820226
 S PAT(.09)="000000121"
 S PAT(.096)=1
 S PAT(.097)=$P($$HTFM^XLFDT($H),".")
 S PAT(.301)="Y"
 S PAT(391)=1
 S PAT(1901)="Y"
 M PATU(2,"+1,")=PAT
 D UPDATE^DIE("","PATU","PATDFN","ERR")
 S GMPDFN=+$G(PATDFN(1))
 Q
 ;
MKPROB ; Add problem
 N PL
 S ACTDIA="872^253.2"
 S GMPNARR="Hypopituitarism (Diabetes Insipidus)"
 S GMPONS="3120101"
 S GMPRP=1
 S PL("DIAGNOSIS")=ACTDIA
 S PL("PATIENT")=GMPDFN
 S PL("NARRATIVE")=GMPNARR
 S PL("STATUS")="A"
 S PL("ONSET")=GMPONS
 S PL("LEXICON")="304837"
 S PL("PROVIDER")=GMPRP
 S PL("LOCATION")=GMPCLIN
 S PL("RECORDED")="3120202"
 D CREATE^GMPLUTL(.PL,.GMPIFN1)
 Q
 ;
PROBNAR ;
 N %,PROB1,PROB2
 D MKPROB
 I GMPIFN1'>0 D FAIL^XTMUNIT("Save failed: "_$G(GMPIFN1(0))) Q
 S %=$$PROBNAR^IBDFBK3(GMPIFN1)
 D CHKEQ^XTMUNIT($P($G(^AUTNPOV(+$P($G(^AUPNPROB(+$G(GMPIFN1),0)),"^",5),0)),"^"),%)
 Q
 ;
PROBDIA ;
 N %
 S %=$$PROBDIA^IBDFBK3(GMPIFN1)
 D CHKEQ^XTMUNIT(+$P($G(^AUPNPROB(+$G(GMPIFN1),0)),"^"),%)
 Q
 ;
RORLO ;
 N ROR
 S RORTMP=$$ALLOC^RORTMP()
 S RORSTDT="3120101",RORENDT=$$NOW^XLFDT
 S %=$$LOAD^RORHL17(.ROR,GMPIFN1)
 D CHKEQ^XTMUNIT(0,%)
 D CHKEQ^XTMUNIT("P",ROR("OBR","COND"))
 D CHKEQ^XTMUNIT($$HL7DATE($$NOW^XLFDT),ROR("OBR","DE"),"DATE ENTERED")
 D CHKEQ^XTMUNIT($P(ACTDIA,U,2),ROR("OBR","DIAG"),"ICD9")
 D CHKEQ^XTMUNIT($$HL7DATE($$NOW^XLFDT),ROR("OBR","DLM"),"DATE LAST MODIFIED")
 D CHKEQ^XTMUNIT($$HL7DATE(GMPONS),ROR("OBR","DOO"),"ONSET")
 D CHKEQ^XTMUNIT($$HL7DATE("3120202"),ROR("OBR","DREC"),"DATE RECORDED")
 D CHKEQ^XTMUNIT($P($G(^AUPNPROB(GMPIFN1,0)),U,6),ROR("OBR","FACIL"),"FACILITY")
 D CHKEQ^XTMUNIT(GMPRP,ROR("OBR","RP"),"RECORDING PROVIDER")
 D CHKEQ^XTMUNIT("A",ROR("OBR","ST"),"STATUS")
 D CHKEQ^XTMUNIT(GMPNARR,ROR("OBX","PR"),"NARARTIVE")
 D CHKEQ^XTMUNIT(GMPNARR,ROR("OBX","PROB"),"PROBLEM")
 Q
 ;
NEPROB ;
 N %
 S %=$$NEPROB^PXRMISE()
 D CHKEQ^XTMUNIT($P(^AUPNPROB(0),U,4),%)
 Q
 ;
PXRMOUT ;
 N PXRMRM,OCC,IFI,TEXT
 S PXRMRM=0
 S OCC(1)=""
 S IFI(1,"DATE")=$$NOW^XLFDT
 S IFI(1,"DAS")=GMPIFN1
 S IFI(1,"CODEP")=$P(ACTDIA,U,2)
 S IFI(1,"STATUS")="A"
 D OUTPUT^PXRMPROB(0,.OCC,.IFI,0,.TEXT)
 D CHKTF^XTMUNIT($D(TEXT(12)),"Provider narrative not defined")
 D CHKEQ^XTMUNIT(" PANHYPOPITUITARISM",$G(TEXT(4)))
 D CHKEQ^XTMUNIT(" Hypopituitarism",$G(TEXT(12)))
 Q
 ;
PXRMPRB ;
 N PXCA,PXCADT,PXCANPOV,PXCAPDX,PXCAPAT
 S PXCAPAT=+GMPDFN,PXCAPDX=""
 S PXCA("ENCOUNTER")=$$NOW^XLFDT
 S PXCA("PROBLEM",DUZ,1)=+ACTDIA_"^"_GMPONS_"^^^^^^^^"_GMPIFN1
 S PXCADT=$$NOW^XLFDT
 D PROBLEM^PXCAPL(.PXCA,0,0)
 D CHKTF^XTMUNIT($D(PXCA("ERROR"))=0,"Unexpected PXCA Errors")
 K PXCA
 S PXCA("PROBLEM",DUZ,1)=+ACTDIA_"^"_GMPONS_"^^^^^^^^"_999999999
 S PXCA("ENCOUNTER")=$$NOW^XLFDT
 D PROBLEM^PXCAPL(.PXCA,0,0)
 D CHKEQ^XTMUNIT("Problem not in file 9000011^999999999",$G(PXCA("ERROR","PROBLEM",DUZ,1,10)),"Expected: Problem not in file")
 S PXCAPAT=+GMPDFN+1
 K PXCA
 S PXCA("PROBLEM",DUZ,1)=+ACTDIA_"^"_GMPONS_"^^^^^^^^"_GMPIFN1
 S PXCA("ENCOUNTER")=$$NOW^XLFDT
 D PROBLEM^PXCAPL(.PXCA,0,0)
 D CHKEQ^XTMUNIT("Problem in file 9000011 is for a different Patient^"_+GMPIFN1,$G(PXCA("ERROR","PROBLEM",DUZ,1,10)),"Expected: problem is for a different patient")
 Q
 ;
PXDIAG ;
 N PXCA,PXCADT,PXCANPOV,PXCAPDX,PXCAPAT
 S PXCAPAT=+GMPDFN,PXCAPDX=""
 S PXCA("DIAGNOSIS",DUZ,1)=+ACTDIA_"^P^^^^^"_GMPIFN1_"^"_GMPNARR
 S PXCADT=$$NOW^XLFDT
 S PXCANPOV=0
 D DIAG^PXCAPOV(.PXCA,0,0)
 D CHKTF^XTMUNIT($D(PXCA("ERROR"))=0,"Unexpected PXCA Errors")
 K PXCA
 S PXCA("DIAGNOSIS",DUZ,1)=+ACTDIA_"^P^^^^^"_999999999_"^"_GMPNARR
 D DIAG^PXCAPOV(.PXCA,0,0)
 D CHKEQ^XTMUNIT("Problem not in file 9000011^999999999",$G(PXCA("ERROR","DIAGNOSIS",DUZ,1,7)),"Expected: Problem not in file")
 S PXCAPAT=+GMPDFN+1
 K PXCA
 S PXCA("DIAGNOSIS",DUZ,1)=+ACTDIA_"^P^^^^^"_GMPIFN1_"^"_GMPNARR
 D DIAG^PXCAPOV(.PXCA,0,0)
 D CHKEQ^XTMUNIT("Problem in file 9000011 is for a different Patient^"_+GMPIFN1,$G(PXCA("ERROR","DIAGNOSIS",DUZ,1,7)),"Expected: problem is for a different patient")
 Q
 ;
ACKQLST ;
 N %,PL,PLY
 S %=$$PLIST^ACKQUTL6(0,0) ;Invalid patient and/or diagnosis
 D CHKEQ^XTMUNIT(0,%)
 S %=$$PLIST^ACKQUTL6(GMPDFN,0) ;Invalid patient and/or diagnosis
 D CHKEQ^XTMUNIT(0,%)
 S %=$$PLIST^ACKQUTL6(0,0) ;Invalid patient and/or diagnosis
 D CHKEQ^XTMUNIT(0,%)
 S %=$$PLIST^ACKQUTL6(GMPDFN,+ACTDIA) ;ACTIVE problem
 D CHKEQ^XTMUNIT("2^"_GMPIFN1,%,"ACTIVE")
 S PL("PROBLEM")=GMPIFN1
 S PL("PROVIDER")=DUZ
 S PL("STATUS")="I"
 D UPDATE^GMPLUTL(.PL,.PLY) ; INACTIVATE PROBLEM
 S %=$$PLIST^ACKQUTL6(GMPDFN,+ACTDIA) ;INACTIVE problem
 D CHKEQ^XTMUNIT("1^"_GMPIFN1,%,"INACTIVE")
 Q
 ;
HL7DATE(HDT) ;
 Q (17+$E(HDT))_$E(HDT,2,7)
XTENT ;
 ;;PROBNAR
 ;;PROBDIA
 ;;RORLO
 ;;NEPROB
 ;;PXRMOUT
 ;;PXRMPRB
 ;;PXDIAG
 ;;ACKQLST
 Q
